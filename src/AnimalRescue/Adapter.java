package AnimalRescue;

public class Adapter {
    private String name;

    private float availableAmountOfMoney;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public float getAvailableAmountOfMoney() {
        return availableAmountOfMoney;
    }

    public void setAvailableAmountOfMoney(float availableAmountOfMoney) {
        this.availableAmountOfMoney = availableAmountOfMoney;
    }
}
