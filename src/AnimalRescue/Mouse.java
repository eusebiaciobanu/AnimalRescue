package AnimalRescue;

public class Mouse extends Animal {

   public void eat() {
        System.out.println("The mouse eats");
    }

   public void sleep() {
        System.out.println("The mouse sleeps");
    }

   public void speak() {
        System.out.println("The mouse speaks");
    }

   public void run() {
        System.out.println("The mouse runs");
    }
}